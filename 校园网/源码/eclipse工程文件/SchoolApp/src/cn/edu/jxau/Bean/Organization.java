package cn.edu.jxau.Bean;

public class Organization {

	private int id;
	private String name;
	private String insititute;
	private String servey;
	private String image;
	private String organtime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getInsititute() {
		return insititute;
	}

	public void setInsititute(String insititute) {
		this.insititute = insititute;
	}

	public String getServey() {
		return servey;
	}

	public void setServey(String servey) {
		this.servey = servey;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getOrgantime() {
		return organtime;
	}

	public void setOrgantime(String organtime) {
		this.organtime = organtime;
	}
}
