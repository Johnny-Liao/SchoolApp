package cn.edu.jxau.Bean;

public class Houseinfo {
	
	private int id;
	private String housename;
	private String houseplace;
	private String housemoney;
	private String housephone;
	private String housetime;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getHousename() {
		return housename;
	}

	public void setHousename(String housename) {
		this.housename = housename;
	}

	public String getHouseplace() {
		return houseplace;
	}

	public void setHouseplace(String houseplace) {
		this.houseplace = houseplace;
	}

	public String getHousemoney() {
		return housemoney;
	}

	public void setHousemoney(String housemoney) {
		this.housemoney = housemoney;
	}

	public String getHousephone() {
		return housephone;
	}

	public void setHousephone(String housephone) {
		this.housephone = housephone;
	}

	public String getHousetime() {
		return housetime;
	}

	public void setHousetime(String housetime) {
		this.housetime = housetime;
	}


}
