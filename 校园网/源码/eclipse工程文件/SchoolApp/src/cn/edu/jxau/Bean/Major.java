package cn.edu.jxau.Bean;

public class Major {
	private int id;
	private String majorname;
	private String insititute;
	private String synopsis;
	private String image;
	private String majortime;

	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}

	public String getMajorname() {
		return majorname;
	}

	public void setMajorname(String majorname) {
		this.majorname = majorname;
	}

	public String getInsititute() {
		return insititute;
	}

	public void setInsititute(String insititute) {
		this.insititute = insititute;
	}

	public String getSynopsis() {
		return synopsis;
	}

	public void setSynopsis(String synopsis) {
		this.synopsis = synopsis;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getMajortime() {
		return majortime;
	}

	public void setMajortime(String majortime) {
		this.majortime = majortime;
	}

}
