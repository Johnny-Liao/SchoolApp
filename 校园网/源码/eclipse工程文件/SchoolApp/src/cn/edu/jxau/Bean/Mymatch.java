package cn.edu.jxau.Bean;

public class Mymatch {
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getInsititute() {
		return insititute;
	}
	public void setInsititute(String insititute) {
		this.insititute = insititute;
	}
	public String getMatchtype() {
		return matchtype;
	}
	public void setMatchtype(String matchtype) {
		this.matchtype = matchtype;
	}
	public String getServey() {
		return servey;
	}
	public void setServey(String servey) {
		this.servey = servey;
	}
	private int id;
	private String insititute;
	private String matchtype;
	private String servey;
}
